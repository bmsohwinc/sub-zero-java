public class RightShifters {

	public static void main(String[] args) {
		
		int a1 = 8;
		System.out.println(a1 + " >> 1: " + (a1 >> 1));

		int a2 = -8;
		System.out.println(a2 + " >> 1: " + (a2 >> 1));
		System.out.println(a2 + " >>> 1: " + (a2 >>> 1));

		int a3 = -9;
		System.out.println(a3 + " >> 1: " + (a3 >> 1));
		System.out.println(a3 + " >>> 1: " + (a3 >>> 1));

		// System.out.println(a1 + " >> 1: " + (a1 >> 1));
		// System.out.println(a1 + " >> 1: " + (a1 >> 1));

	}

}
